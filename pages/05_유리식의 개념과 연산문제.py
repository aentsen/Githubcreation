import streamlit as st

# 유리식 개념 설명
def show_concept():
    st.title("유리식의 개념")
    st.write("""
    **유리식**은 두 다항식의 비율로 표현되는 수학적인 식입니다. 즉, 유리식은 다음과 같은 형태를 가집니다:
    
    f(x) = (P(x)) / (Q(x))
    
    여기서 P(x)와 Q(x)는 각각 다항식이고, Q(x)는 0이 될 수 없습니다. 예를 들어, f(x) = (2x+1) / (x^2+3x+2)와 같은 식이 유리식입니다.
    
    ### 유리식의 주요 특징
    1. **분수 형태**: 유리식은 항상 분수로 표현됩니다.
    2. **분모와 분자가 다항식**: 분모와 분자는 모두 다항식입니다.
    3. **분모가 0이 될 수 없음**: 유리식에서 분모가 0이 되면 식이 정의되지 않습니다.
    """)

# 유리식 연산 개념 설명
def show_operations_concept():
    st.title("유리식의 연산")
    st.write("""
    유리식의 연산은 **덧셈, 뺄셈, 곱셈, 나눗셈**이 있습니다. 각 연산의 방법을 살펴보겠습니다.

    ### 덧셈과 뺄셈
    두 유리식을 더하거나 뺄 때, 분모를 같게 만든 후, 분자끼리 더하거나 뺍니다.
    
    예를 들어:
    (a) / (b) + (c) / (b) = (a+c) / (b)
    
    ### 곱셈
    두 유리식을 곱할 때는 분자끼리, 분모끼리 각각 곱합니다.
    
    예를 들어:
    (a) / (b) × (c) / (d) = (ac) / (bd)
    
    ### 나눗셈
    나누는 유리식의 분모와 분자를 뒤집어서 곱합니다. 즉, 나누기 문제는 곱셈으로 바꿔서 해결합니다.
    
    예를 들어:
    (a) / (b) ÷ (c) / (d) = (a) / (b) × (d) / (c) = (ad) / (bc)
    """)

# 유리식 개념 적용 문제
def show_conceptual_problem():
    st.title("유리식 개념 적용 문제")
    st.subheader("문제 1: 유리식의 정의를 이해합시다.")
    st.write("""
    다음 중 유리식에 해당하는 식은 무엇인가요?
    """)
    answer_1 = st.radio("정답을 선택하세요:", ("(x+2) / (x^2+1)", "(x^2 + 2x + 1)", "(x+1) / (x-1)", "(x^2 + 3x + 2)"))
    
    if answer_1:
        if answer_1 == "(x+2) / (x^2+1)":
            st.success("정답입니다! 유리식은 분모와 분자가 다항식인 분수 형태로 나타납니다.")
        else:
            st.error("틀렸습니다. 정답은 (x+2) / (x^2+1)입니다.")

# 유리식 연산 적용 문제
def show_operations_problem():
    st.title("유리식 연산 적용 문제")
    
    st.subheader("문제 1: 유리식 덧셈")
    st.write("(2x+3) / (x+1) + (x-1) / (x+1)")
    answer_1 = st.radio("정답을 선택하세요:", ("1. (3x+2) / (x+1)", "2. (3x+4) / (x+1)", "3. (2x+4) / (x+2)", "4. (3x+4) / (x+2)", "5. (2x+2) / (x+1)"))
    
    if answer_1:
        if answer_1 == "2. (3x+4) / (x+1)":
            st.success("정답입니다! 덧셈을 하려면 분모를 같게 만든 후 분자끼리 더해야 합니다.")
        else:
            st.error("틀렸습니다. 정답은 (3x+4) / (x+1)입니다.")

    st.write("---")
    
    st.subheader("문제 2: 유리식 곱셈")
    st.write("(x+1) / (x-2) × (2x-3) / (x+4)")
    answer_2 = st.radio("정답을 선택하세요:", ("1. (2x^2 - 5x + 3) / (x^2 + 2x - 8)", "2. (2x^2 - 5x - 12) / (x^2 + 2x - 8)", "3. (2x^2 - 7x + 12) / (x^2 + 2x - 8)", "4. (2x^2 + 3x - 6) / (x^2 - 4x - 8)", "5. (2x^2 - 3x + 6) / (x^2 + 2x - 8)"))
    
    if answer_2:
        if answer_2 == "1. (2x^2 - 5x + 3) / (x^2 + 2x - 8)":
            st.success("정답입니다! 유리식 곱셈에서는 분자끼리, 분모끼리 곱합니다.")
        else:
            st.error("틀렸습니다. 정답은 (2x^2 - 5x + 3) / (x^2 + 2x - 8)입니다.")
    
    st.write("---")
    
    st.subheader("문제 3: 유리식 나눗셈")
    st.write("(x+3) / (x-1) ÷ (x-2) / (x+5)")
    answer_3 = st.radio("정답을 선택하세요:", ("1. (x+3)(x+5) / (x-1)(x-2)", "2. (x+3)(x-2) / (x-1)(x+5)", "3. (x+3)(x-2) / (x-1)(x-5)", "4. (x+3)(x+5) / (x-1)(x+2)", "5. (x+3)(x+5) / (x-2)(x-1)"))
    
    if answer_3:
        if answer_3 == "1. (x+3)(x+5) / (x-1)(x-2)":
            st.success("정답입니다! 나누기를 곱셈으로 바꾸어서 처리합니다.")
        else:
            st.error("틀렸습니다. 정답은 (x+3)(x+5) / (x-1)(x-2)입니다.")

# 메인 함수
def main():
    show_concept()
    st.write("---")
    show_operations_concept()
    st.write("---")
    show_conceptual_problem()
    st.write("---")
    show_operations_problem()

if __name__ == "__main__":
    main()
